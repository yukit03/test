//int型の変数x、y、zに数値18、49、118をそれぞれ代入してください。
//実行結果通りになるようにコンソール画面に表示させましょう。
public class Main {
	
	public static void main(String[] args) {
		int x = 18;
		int y = 49;
		int z = 118;
		System.out.println("x × y + z = " + (x * y + z));
		System.out.println("x × (y + z) = " + (x * (y + z)));
	}

}
