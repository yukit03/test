package print6.questions01;

public class Main {
	
	/*
	 * メソッド名： printHello
		引数：なし
		戻り値：なし
		処理の内容： "Hello" という文字を出力表示する
		ヒント：メソッドの定義は次のようになる
		public static void printHello() { 処理コード記入 }
	 */
	public static void printHello() {
		
		System.out.println("Hello");
	}
	
	/*
	 * メソッド名：main
	　 引数：String[] args
	　 戻り値：なし
	　 処理の内容：printHelloメソッドを呼び出す
	 */
	public static void main(String[] args) {
		
		printHello();
		
	}
}
